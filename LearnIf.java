package week1.day1;

public class LearnIf {

	public static void main(String[] args) {
		
		int i=0;
		
		if(i%2==0) {
			
			System.out.println("The number is even");
		}
		
		else if(i%2!=0) {
			System.out.println("The number is odd");
		}
		
		else {
			
			System.out.println("The number is neither even nor odd");
			
		}
		

	}

}
